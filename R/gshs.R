#' Global School-Based Student Health Survey, Buenos Aires, 2018
#'
#' @description
#'
#' From [WHO](https://extranet.who.int/ncdsmicrodata/index.php/catalog/866/study-description):
#'
#' The GSHS is a school-based survey which uses a self-administered
#' questionnaire to obtain data on young people's health behaviour and
#' protective factors related to the leading causes of morbidity and mortality
#' among children and adults worldwide.
#'
#' This is the third GSHS conducted by Argentina.
#'
#' ### Sampling procedure
#'
#' A two-stage cluster sample design was used to produce data representative of
#' all students in 8 primary/polimodal 1st high - 12 polimodal 5th high in
#' Argentina. At the first stage, schools were selected with probability
#' proportional to enrollment size. At the second stage, classes were randomly
#' selected and all students in selected classes were eligible to participate.
#'
#' ### Response rate
#' The school response rate was 86%, the student response rate was 74%, and the
#' overall response rate was 63%. A total of 56,981 students participated in the
#' Argentina GSHS.
#'
#' This dataset includes only respondents from Buenos Aires, the capital and
#' largest city in Argentina.
#'
#' @source <https://extranet.who.int/ncdsmicrodata/index.php/catalog/866/study-description>
#' @family GSHS datasets
#'
